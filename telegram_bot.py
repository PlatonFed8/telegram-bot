def calculate_results(update: Update, context: CallbackContext) -> None:
    matching_answers = sum(
        1 for i in range(len(context.user_data['questions']))
        if context.user_data['answers'].get(i) == context.user_data['partner_answers'].get(i)
    )
    percentage = (matching_answers / len(context.user_data['questions'])) * 100

    if percentage > 70:
        emoji = "üòÑ"
    elif percentage < 30:
        emoji = "üò¢"
    else:
        emoji = "üôÇ"

    result_message = f"–í–∞—à –ø—Ä–æ—Ü–µ–Ω—Ç —Å–æ–≤–ø–∞–¥–µ–Ω–∏–π: {percentage:.1f}% {emoji}\n"
    result_message += (
        "–í—ã –ø—Ä–µ–∫—Ä–∞—Å–Ω–æ –∑–Ω–∞–µ—Ç–µ –ø—Ä–µ–¥–ø–æ—á—Ç–µ–Ω–∏—è –¥—Ä—É–≥ –¥—Ä—É–≥–∞!" if percentage > 70 else
        "–ù–µ–ø–ª–æ—Ö–æ, –Ω–æ –µ—Å—Ç—å –Ω–∞–¥ —á–µ–º —Ä–∞–±–æ—Ç–∞—Ç—å." if percentage > 50 else
        "–í–∞–º —Å—Ç–æ–∏—Ç –±–æ–ª—å—à–µ —É–∑–Ω–∞—Ç—å –¥—Ä—É–≥ –¥—Ä—É–≥–∞."
    )

    keyboard = [
        [InlineKeyboardButton("–°–ª–µ–¥—É—é—â–∞—è –∏–≥—Ä–∞", callback_data="next_game")],
        [InlineKeyboardButton("–ü–æ—Å–º–æ—Ç—Ä–µ—Ç—å –æ—Ç–≤–µ—Ç—ã", callback_data="view_answers")]
    ]
    reply_markup = InlineKeyboardMarkup(keyboard)
    update.callback_query.message.reply_text(result_message, reply_markup=reply_markup)

