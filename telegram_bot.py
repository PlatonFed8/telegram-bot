    update.message.reply_text("–¢–µ–ø–µ—Ä—å –≤–∞—à –ø–∞—Ä—Ç–Ω–µ—Ä –æ—Ç–≤–µ—á–∞–µ—Ç –Ω–∞ —Ç–µ –∂–µ –≤–æ–ø—Ä–æ—Å—ã.")
    ask_question(update, context, 0)

